i=1
while i<=10:   #printing numbers by using while loop
  print(i)     #the numbers will be print in a vertical way
  i+=1


i=1
while i<=10:         
  print(i,end=' ')    #the numbers will be print in single horizontal line  
  i+=1   


#printing numbers by using for loop
for i in range(11):   #can also write for i in range(1,11) 
   print(i)           


#this method will print numbers in the form of 'List'
print([i for i in range(1,11)])
