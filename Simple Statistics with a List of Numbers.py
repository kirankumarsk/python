#Simple Statistics with a List of Numbers
#Creating a List of numbers form 1 to 100
values=list(range(1,100))
print(values)

print('To Find Max of the numbers')
maximum=max(values)  #Findinding the Maxmum number from the list
minimum=min(values)  #Finding the minimum number form the list
sumation=sum(values)  #Finding the total(sumation) from the list

print(maximum)
print(minimum)
print(sumation)
